let LANG = (function(){ try{ return localStorage.getItem(LANG_KEY) || 'el'; }catch(e){ return 'el'; } })();
function getLang(){ return LANG; }
function tr(k, vars){
  const table = I18N[LANG] || I18N.el;
  let s = (table[k] != null) ? table[k] : (I18N.el[k] != null ? I18N.el[k] : k);
  if(vars){ for(const key in vars){ s = s.split('{'+key+'}').join(vars[key]); } }
  return s;
}
function setLang(code){ if(!I18N[code]) return; LANG = code; try{ localStorage.setItem(LANG_KEY, code); }catch(e){} }
function applyStaticI18n(root){
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = tr(el.getAttribute('data-i18n')); });
  root.querySelectorAll('[data-i18n-ph]').forEach(el => { el.setAttribute('placeholder', tr(el.getAttribute('data-i18n-ph'))); });
  root.querySelectorAll('[data-i18n-title]').forEach(el => { el.setAttribute('title', tr(el.getAttribute('data-i18n-title'))); });
  try{ document.documentElement.lang = LANG; }catch(e){}
}
